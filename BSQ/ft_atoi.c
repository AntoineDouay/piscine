int	ft_atoi(char *str)
{
	int					m;
	long long int		res;

	m = 1;
	res = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	while (*str == '+' || *str == '-')
		{
			if (*str == '-')
				m = m * -1;
			str++;
		}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - 48);
		str++;
	}
	return (res * m);
}

