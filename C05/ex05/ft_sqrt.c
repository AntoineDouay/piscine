#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb < 0)
		return (0);
	while (i <= 46340 && i * i <= nb)
	{
		if (i * i == nb)
			if (i % i == 0)
				return (i);
		i++;
	}
	return (0);
}
